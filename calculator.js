let firstNumber = 10;
let secondNumber = 5 ;
let operation = "+";

if (operation == "+"){

    let calc = firstNumber+secondNumber;
    console.log(calc);
} else if (operation == "-"){

    let calc = firstNumber-secondNumber;
    console.log(calc);
} else if (operation == "*"){
    let calc = firstNumber*secondNumber;
    console.log(calc);
} else if (operation == "/"){
    let calc = firstNumber/secondNumber;
    console.log(calc);
}
